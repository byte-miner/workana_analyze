export function sortFieldsByRank(fields: ProjectField[]): ProjectField[] {
  return [...fields].sort((a, b) => b.demandScore - a.demandScore);
}

export function getFieldsByCategory(category: FieldCategory): ProjectField[] {
  return sortFieldsByRank(projectFields.filter((f) => f.category === category));
}

export const fieldsGroupedByCategory = FIELD_CATEGORIES.map((category) => ({
  category,
  group: FIELD_GROUP_MAP[category],
  fields: getFieldsByCategory(category),
}));

export const topFieldsByDemand = sortFieldsByRank(projectFields).slice(0, 12);

export function getTopFieldsByCategory(category: FieldCategory, limit = 8): ProjectField[] {
  return getFieldsByCategory(category).slice(0, limit);
}
